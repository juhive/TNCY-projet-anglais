package Tools;

public class Quintuplet {

    public String vb;
    public String ptr;
    public String pp;
    public String t1;
    public String t2;

    public Quintuplet(String vb, String ptr, String pp, String t1, String t2) {
        this.vb = vb;
        this.ptr = ptr;
        this.pp = pp;
        this.t1 = t1;
        this.t2 = t2;
    }

	public String getVb() {
		return vb;
	}

	public void setVb(String vb) {
		this.vb = vb;
	}

	public String getPtr() {
		return ptr;
	}

	public void setPtr(String ptr) {
		this.ptr = ptr;
	}

	public String getPp() {
		return pp;
	}

	public void setPp(String pp) {
		this.pp = pp;
	}

	public String getT1() {
		return t1;
	}

	public void setT1(String t1) {
		this.t1 = t1;
	}

	public String getT2() {
		return t2;
	}

	public void setT2(String t2) {
		this.t2 = t2;
	}


}
